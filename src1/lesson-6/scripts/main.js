const getNewObj = (arr) => {
  return arr.reduce((acc, hotel) => {
    if (acc[hotel.country]) {
      return {
        ...acc,
        [hotel.country]: [...acc[hotel.country], hotel.city],
      };
    }
    return {
      ...acc,
      [hotel.country]: [hotel.city],
    };
  }, {});
};

